const countOnly = function(allItems, itemsToCount) {         // checks the array, items to compare with
  const results = {}                                         // creat var to return
  
  for (const item of allItems) {                             // for of loop. looks for items in our array
        
  if (itemsToCount[item]) {                                  // checks for single item from allItems, in items to count  
      if (results[item]) {                                   // if item is found add 1 to results
        results[item] += 1;
      }   else {
        results[item] = 1;
      }
    }
  }
  return results;
};


